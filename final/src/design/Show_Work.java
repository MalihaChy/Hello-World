    void Show_Work_List()
    {
        DefaultListModel model1 = new DefaultListModel();
        char Initial=addcontact.WORK.get(0).charAt(0);
        model1.addElement(Initial);
        for (int i = 0; i < addcontact.WORK.size(); i++) //
        {
            if(Initial==addcontact.WORK.get(i).charAt(0))
            model1.addElement(addcontact.WORK.get(i));
            else{
                Initial=addcontact.WORK.get(i).charAt(0);
                model1.addElement(" ");
                model1.addElement(Initial);
                model1.addElement(addcontact.WORK.get(i));
            }
        }
        jList1.setModel(model1); 
    }

